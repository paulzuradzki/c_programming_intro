// using basic data types

#include <stdio.h>

int main (void)
{
    int integerVar = 100;
    float floatVar = 331.79;
    double doubleVar = 8.44e+11;
    char charVar = 'W';

    _Bool boolVar = 1;

    printf ("int: %i \n", integerVar);
    printf ("float: %f \n", floatVar);
    printf ("double: %e \n", doubleVar);
    printf ("double: %g \n", doubleVar);
    printf ("char: %c \n", charVar);
    printf ("bool: %i \n", boolVar);

    return 0;
}


